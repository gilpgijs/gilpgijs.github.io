let z = 0;
/* Postincremento. Muestra el valor
 * después del incremento, o sea 1 */
console.log(++z);
// z === 1
/* Preincremento. Muestra el valor
 * antes del incremento, o sea 1 */
console.log(z++);
// z === 2
/* Predecremento. Muestra el valor
 * antes del decremento, o sea 2 */
console.log(z--);
// z === 1
/* Postdecremento. Muestra el valor
 * después del decremento, o sea 0. */
console.log(--z);
// z === 0